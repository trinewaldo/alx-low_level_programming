#include <stdio.h>

/**
 * add - does addition function
 * @a: first int
 * @b: second int
 * Return: addition
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - does subtraction function
 * @a: first int
 * @b: second int
 * Return: subtraction
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * div - does division function
 * @a: first int
 * @b: second int
 * Return: division
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - does modulus function
 * @a: first int
 * @b: second int
 * Return: modulus
 */
int mod(int a, int b)
{
	return (a % b);
}

/**
 * mul - does multiplication function
 * @a: first int
 * @b: second int
 * Return: multiplication
 */
int mul(int a, int b)
{
	return (a * b);
}
